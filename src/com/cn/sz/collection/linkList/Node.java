package com.cn.sz.collection.linkList;

public class Node {
	private Node provious;
	private Object obj;
	private Node next;

	public Node getProvious() {
		return provious;
	}

	public void setProvious(Node provious) {
		this.provious = provious;
	}

	public Object getObj() {
		return obj;
	}

	public void setObj(Object obj) {
		this.obj = obj;
	}

	public Node getNext() {
		return next;
	}

	public void setNext(Node next) {
		this.next = next;
	}

}
